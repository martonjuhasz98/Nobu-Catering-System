package modlayer;

public class City {

	private String zipCode;
	private String name;
	
	public City() {
		
	}

	public String getZipCode() {
		return zipCode;
	}
	public String getName() {
		return name;
	}
	public void setZipCode(String zipCode) {
		this.zipCode = zipCode;
	}
	public void setName(String name) {
		this.name = name;
	}
}
