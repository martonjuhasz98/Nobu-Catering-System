package modlayer;

public class Unit {

	private String abbr;
	private String name;
	
	public Unit() {
		
	}

	public String getAbbr() {
		return abbr;
	}
	public String getName() {
		return name;
	}
	public void setAbbr(String abbr) {
		this.abbr = abbr;
	}
	public void setName(String name) {
		this.name = name;
	}
}
